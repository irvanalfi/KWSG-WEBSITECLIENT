                        Koperasi Warga Semen Gresik (KWSG) merupakan koperasi karyawan di lingkungan PT. Semen Gresik yang bergerak di bidang perdagangan barang dan jasa. Sejak didirikan pada tanggal 29 Januari 1963, dengan nama: Koperasi Serba Usaha Semen Gresik, kemudian menjadi sebuah badan hukum pada tanggal 17 Desember 1967, dengan nomor Badan Hukum : 1545/BH/II/12.67. Awal mula dibentuknya KWSG adalah untuk memenuhi kebutuhan karyawan Semen Gresik yang pada saat itu masih jauh dari lokasi pasar. Sehingga usaha pokoknya menjual bahan pokok dan sayuran.
                        <br>
                        <br>
                        Seiring dengan makin beragamnya usahanya saat itu (Simpan Pinjam Percetakan, Foto copy, Jasa Boga dan Perdagangan Umum), maka sejak tahun 1991, Koperasi Serba Usaha Semen Gresik berubah nama menjadi Koperasi Warga Semen Gresik (KWSG).
                        <br>
                        <br>
                        Dengan makin pesatnya pertumbuhan perekonomian Indonesia, bisnis property pun mulai menunjukkan geliatnya. KWSG juga turut berbenah menyesuaikan diri dengan tuntutan bisnis yang sedang booming saat itu demi mencapai kesejahteraan anggota. Sehingga pada tahun 1992, KWSG mendirikan bisnis Perdagangan Bahan Bangunan yang menjual produk Semen Gresik dan nonsemen (besi, asbes, paku, triplek). Untuk mendukung proses distribusi dari supplier ke gudang PBB KWSG, supplier ke pelanggan, maka tahun 1993 KWSG menambahkan bisnis ekspedisi dalam lini bisnisnya.KWSG terus mengukir prestasinya dengan terus melakukan akselerasi perkembangan dan pertumbuhannya. Pada 2015 KWSG telah tumbuh dan berkembang hingga menjadi 75 cabang yang tersebar di P. Jawa, P. Bali, P. Lombok, P. Sumatra, Sulawesi, hingga P. Kalimantan.
                        <br>
                        <br>
                        Eksistensi KWSG dalam kancah bisnis nasional mendapat apresiasi dari Kementerian Kekoperasian dan UKM dengan diundangnya KSWG ke Jakarta untuk penyerahan Nomor Induk Koperasi ( NIK) oleh Menteri Kekoperasian dan UKM, AA Gede Ngurah Puspayugo pada tanggal 25 Mei 2015.
                        <br>
                        <br>
                        Dalam perjalanan meniti bisnisnya, KWSG mengepakkan sayap bisnisnya, dengan melakukan perubahan Anggaran Dasar dan Rumah Tangga (AD & ART) serta melakukan penataan organisasi, manajemen dan usaha yang hingga tahun 2015, KWSG telah memiliki 10 lini bisnis, yaitu:
                        <br>
                        <ol>
                            <li>Even Organizer dan Office Printing (Garis Comm)</li>
                            <li>Retail (V-mart Swalayan dan mini V-mart)</li>
                            <li>Restaurant (The Legen Resto)</li>
                            <li>Perdagan Umum dan Industri</li>
                            <li>Perdagan Bahan Bangunan</li>
                            <li>Perdagan Barang Tambang</li>
                            <li>Pabrik FCB "Gress Board"</li>
                            <li>Simpan Pinjam</li>
                            <li>Expedisi</li>
                            <li>Penjahitan</li>
                        </ol>
                        <br>
                        Dengan total pendapatan Rp2,69 T dan SHU setelah pajak Rp50,33 M, KWSG telah memposisikan dirinya sebagai koperasi terbesar ke-2 se- Indonesia. KWSG juga merupakan koperasi pertama di Indonesia yang masuk dalam The Top 300 Co-operative and Mutual Enterprises oleh Global World Co-operative dan menduduki peringkat 210 pada tahun 2014. Prestasi membanggakan ini juga telah dicatat dalam MURI.
